    protected void datatable(){
        Object[] Baris = {"ID Kasir", "Nama", "Jenis Kelamin", "Alamat", "No. Telepon", "Password", "Tanggal Masuk"};
            model = new DefaultTableModel(null, Baris);
            String cariitem=txtcari.getText();
            
            try{
                String sql = "SELECT * FROM kasir WHERE id_kasir LIKE '%" + cariitem + "%' " + "OR nama_kasir LIKE '%" + cariitem + "%' " + "OR jenis_kelamin LIKE '%" + cariitem + "%' " + "OR alamat LIKE '%" + cariitem + "%' " + "OR no_telp LIKE '%" + cariitem + "%' "  + "OR password LIKE '%" + cariitem + "%' " + "ORDER BY id_kasir ASC";
                Statement stat = koneksi.createStatement();
                ResultSet hasil = stat.executeQuery(sql);
                
                while (hasil.next()){
                    model.addRow(new Object[]{
                        hasil.getString(1),
                        hasil.getString(2),
                        hasil.getString(3),
                        hasil.getString(4),
                        hasil.getString(5),
                        hasil.getString(6),
                        hasil.getString(7)
                    });
                }
                tablekasir.setModel(model);
            }catch (Exception e){
                JOptionPane.showMessageDialog(null, "Data gagal dipanggil"+e);
            }
    }

