    private void bsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsimpanActionPerformed
        String jenis = null ;
    if (rlaki.isSelected()) {
    jenis = "Laki-Laki";
} else if (rperempuan.isSelected()) {
    jenis = "Perempuan";
}

    if (txtid.getText().equals("") || txtnm.getText().equals("") || txttelp.getText().equals("") || txtalamat.getText().equals("") || jenis == null) {
        JOptionPane.showMessageDialog(null, "Lengkapi semua data termasuk jenis kelamin!");
        return;
    }

    String sql = "insert into pelanggan values(?,?,?,?,?)";
    try{
        PreparedStatement stat = conn.prepareStatement(sql);
        stat.setString(1, txtid.getText());
        stat.setString(2, txtnm.getText());
        stat.setString(3, jenis);
        stat.setString(4, txttelp.getText());
        stat.setString(5, txtalamat.getText());

        stat.executeUpdate();
        JOptionPane.showMessageDialog(null,"Data berhasil disimpan");
        kosong();
        txtid.requestFocus();
    } catch (SQLException e){
        JOptionPane.showMessageDialog(null,"Data gagal disimpan: " + e.getMessage());
    }
    datatable();    
    }//GEN-LAST:event_bsimpanActionPerformed

    private void bubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bubahActionPerformed
        String jenis = null;
        if (rlaki.isSelected()){
            jenis = "Laki-Laki";
        }else if (rperempuan.isSelected()){
            jenis = "Perempuan";
        }
        try{
            String sql = "update pelanggan set nmplg=?, jk=?, telp=?, alamat=? where id=?";
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, txtnm.getText());
            stat.setString(2, jenis);
            stat.setString(3, txttelp.getText());
            stat.setString(4, txtalamat.getText());

            stat.executeUpdate();
            JOptionPane.showMessageDialog(null,"data berhasil diubah");
            kosong();
            txtid.requestFocus();
        }
        catch (SQLException e){
            JOptionPane.showMessageDialog(null,"data gagal diubah"+e);
        }
        datatable();
    }//GEN-LAST:event_bubahActionPerformed

