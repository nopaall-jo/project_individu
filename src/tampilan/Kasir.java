    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        // TODO add your handling code here:
        String jenis = null;
    if (klaki.isSelected()) {
        jenis = "Laki-Laki";
    } else if (kperempuan.isSelected()) {
        jenis = "Perempuan";
    }

    // Validasi input
    if (txtid.getText().isEmpty() || txtnama.getText().isEmpty() || jenis == null ||
        txtalamat.getText().isEmpty() || txttelp.getText().isEmpty() || txtpass.getText().isEmpty()) {
        JOptionPane.showMessageDialog(null, "Lengkapi semua field terlebih dahulu!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        return;
    }

    String sql = "INSERT INTO kasir VALUES (?, ?, ?, ?, ?, ?, ?)";
    try {
            PreparedStatement stat = koneksi.prepareStatement(sql);
            stat.setString(1, txtid.getText());
            stat.setString(2, txtnama.getText());
            stat.setString(3, jenis);
            stat.setString(4, txtalamat.getText());
            stat.setString(5, txttelp.getText());
            stat.setString(6, txtpass.getText());
            stat.setString(7, txttanggal.getText());
            
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
            kosong();
            txtid.requestFocus();
        }
        catch (SQLException e){
            JOptionPane.showMessageDialog(null, "Data gagal disimpan"+e);
        }
        datatable();
    }//GEN-LAST:event_btnsimpanActionPerformed

